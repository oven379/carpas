    /**
     * @param  array<int, mixed>  $flat
     * @return array{det: list<string>, maint: list<string>}
     */
    public static function splitFlatToBuckets(array $flat): array
    {
        $det = [];
        $maint = [];
        $seenD = [];
        $seenM = [];
        foreach ($flat as $raw) {
            $s = trim((string) $raw);
            if ($s === '') {
                continue;
            }
            $inD = in_array($s, self::DETAILING_ITEMS, true);
            $inM = in_array($s, self::MAINTENANCE_ITEMS, true);
            $goMaint = $inM && ! $inD;
            if ($goMaint) {
                $k = mb_strtolower($s);
                if (! isset($seenM[$k])) {
                    $seenM[$k] = true;
                    $maint[] = $s;
                }
            } else {
                $k = mb_strtolower($s);
                if (! isset($seenD[$k])) {
                    $seenD[$k] = true;
                    $det[] = $s;
                }
            }
        }

        return ['det' => array_values($det), 'maint' => array_values($maint)];
    }
