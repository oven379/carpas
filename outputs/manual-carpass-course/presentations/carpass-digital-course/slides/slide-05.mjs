import { C, header, footer, card, box, text, notes } from './common.mjs'

export async function slide05(presentation) {
  const slide = presentation.slides.add()
  header(slide, 'Что уже закрывает КарПас CRM', 'продукт')
  const features = [
    ['Кабинет сервиса', 'Детейлинг / СТО ведет автомобили, клиентов и визиты.'],
    ['Клиенты и авто', 'Поиск по имени, телефону, VIN и госномеру, заметки по клиенту.'],
    ['История визитов', 'Пробег, услуги, ТО, фото, документы, рекомендации мастера.'],
    ['Повторные контакты', 'Дата следующего контакта и фильтр “давно не были”.'],
    ['Публичный лендинг', 'Город, адрес, услуги, режим работы, соцсети, фото и обложка.'],
    ['Связь с владельцем', 'Гараж владельца, заявки на привязку авто, история по ссылке.'],
  ]
  features.forEach((f, i) => {
    const col = i % 2
    const row = Math.floor(i / 2)
    card(slide, f[0], f[1], 64 + col * 560, 185 + row * 130, 500, 102, { titleColor: i === 5 ? C.green : C.ink })
  })
  box(slide, 64, 600, 1060, 48, { round: true, fill: C.green2, stroke: C.green2, text: 'КарПас — не просто “список клиентов”: он связывает сервис, автомобиль и владельца через историю обслуживания.', size: 23, bold: true, color: C.green, align: 'center' })
  footer(slide, 5)
  notes(slide, 'Важно: говорить только о том, что уже есть или видно в проекте. Не обещать кассу, склад, IP-телефонию и сквозную аналитику как готовые встроенные функции. Их можно назвать как будущие или внешние элементы цифровой системы.')
  return slide
}
