import { C, header, footer, box, text, notes } from './common.mjs'

export async function slide11(presentation) {
  const slide = presentation.slides.add()
  header(slide, 'Что внедрить за 7 дней', 'план')
  const days = [
    ['День 1', 'Подключить КарПас и заполнить профиль сервиса'],
    ['День 2', 'Добавить услуги, адрес, режим работы и публичную страницу'],
    ['День 3', 'Завести первые 10-20 клиентов и автомобилей'],
    ['День 4', 'Начать фиксировать визиты: услуги, пробег, фото'],
    ['День 5', 'Поставить следующий контакт по каждому активному клиенту'],
    ['День 6', 'Попросить отзывы у свежих клиентов и обновить карты'],
    ['День 7', 'Посмотреть базу: кто давно не был и кого вернуть'],
  ]
  days.forEach((d, i) => {
    const y = 180 + i * 62
    box(slide, 82, y, 118, 42, { round: true, fill: i === 0 ? C.green : C.white, stroke: i === 0 ? C.green : C.line, text: d[0], size: 19, bold: true, color: i === 0 ? C.white : C.green, align: 'center' })
    text(slide, d[1], 228, y + 4, 820, 32, { size: 24 })
  })
  footer(slide, 11)
  notes(slide, 'Это финальный практический блок большой версии. Важно не перегружать: цель не идеальная автоматизация, а быстрый старт, после которого сервис уже видит клиентов, авто и повторные контакты.')
  return slide
}
