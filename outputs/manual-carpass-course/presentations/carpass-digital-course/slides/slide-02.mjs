import { C, header, footer, card, notes } from './common.mjs'

export async function slide02(presentation) {
  const slide = presentation.slides.add()
  header(slide, 'Сервис теряет деньги не только из-за слабой рекламы', 'карта потерь')
  const items = [
    ['Заявки', 'Пишут в разные мессенджеры, часть обращений остается без ответа.'],
    ['Запись', 'Время, клиент, авто и ответственный держатся в голове или блокноте.'],
    ['История', 'Непонятно, что делали с машиной раньше и какие рекомендации давали.'],
    ['Повторы', 'Клиент уехал после услуги — и сервис ждет, что он сам вспомнит вернуться.'],
    ['Реклама', 'Клики есть, а какие каналы дают реальных клиентов — не видно.'],
    ['Отзывы', 'Отзыв просят случайно, репутация на картах растет медленно.'],
  ]
  items.forEach((it, i) => {
    const col = i % 3
    const row = Math.floor(i / 3)
    card(slide, it[0], it[1], 64 + col * 380, 200 + row * 170, 340, 130, { fill: i % 2 ? C.white : '#FBFCF9' })
  })
  footer(slide, 2)
  notes(slide, 'Главный тезис: до внедрения CRM нужно признать точки потерь. Попросить зал мысленно отметить, где они узнают себя. Здесь хорошо работают живые примеры: заявка в WhatsApp, администратор ушел, владелец не понял, кто должен перезвонить.')
  return slide
}
